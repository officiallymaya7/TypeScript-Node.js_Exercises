// Store a person's name with whitespace
let personNameWithWhitespace = "\t   Maya Khurshid Anwar\n";

// Print the name with whitespace
console.log("Name with Whitespace: '" + personNameWithWhitespace + "'");

// Strip whitespace and print the name
let strippedName = personNameWithWhitespace.trim();
console.log("Stripped Name: '" + strippedName + "'");
//Description : This program demonstrates storing a person's name with whitespace, printing the name with whitespace, and then printing the name after stripping the whitespace.

